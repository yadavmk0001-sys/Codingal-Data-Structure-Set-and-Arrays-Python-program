# Creating two sets
set1 = {10, 20, 30, 40, 50}
set2 = {30, 40, 50, 60, 70}

# Union(|)
print("Union : ",set1 | set2)

# Intersection (&)
print("Intersection : ",set1 & set2)

# Difference (-)
print("Set 1 - Set2 : ",set1 - set2)
print("Set2 - Set1 : ",set2 - set1)

# Symmetric Difference (^)
print("Symmetric difference : ",set1 ^ set2)